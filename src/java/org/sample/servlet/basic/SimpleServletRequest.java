    public void parse()
    {
        try {
            String requestString = content.toString() ;
            String[] lines = requestString.split("\n");
            String reqType = lines[0];
            String[] params = reqType.split(" / ");
            REQUEST_TYPE = params[0];
            PROTOCOL = params[1];
            String[] hostData = lines[1].split(": ");
            if(hostData[0].equalsIgnoreCase("HOST"))
            {
                HOST = hostData[1];
            }


        }catch(Exception e)
        {

        }
    }


